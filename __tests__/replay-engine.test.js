// ── Test Helpers ─────────────────────────────────────────────────────────

function buildTestEvent(overrides) {
  return {
    id: `test-${Math.random().toString(36).substring(2, 9)}`,
    type: 'POLICY_CREATED',
    policyId: 'POL-TEST',
    source: 'SYSTEM',
    reasoning: 'Test event',
    payload: { name: 'Test Policy', premium: 1000, coverageLimit: 50000, status: 'Draft' },
    timestamp: '2026-01-01T00:00:00.000Z',
    metadata: { sequence: 1 },
    ...overrides,
  };
}

function buildEventStream() {
  return [
    buildTestEvent({
      id: 'evt-001',
      type: 'POLICY_CREATED',
      timestamp: '2026-01-01T00:00:00.000Z',
      payload: { name: 'Auto Policy', premium: 1000, coverageLimit: 50000, status: 'Draft' },
      reasoning: 'New policy creation for auto insurance',
      metadata: { sequence: 1 },
    }),
    buildTestEvent({
      id: 'evt-002',
      type: 'POLICY_QUOTED',
      timestamp: '2026-01-02T00:00:00.000Z',
      payload: { premium: 1200 },
      reasoning: 'Initial quote generated by rating engine',
      metadata: { sequence: 2 },
    }),
    buildTestEvent({
      id: 'evt-003',
      type: 'POLICY_BOUND',
      timestamp: '2026-01-03T00:00:00.000Z',
      payload: { status: 'Active' },
      reasoning: 'Policy bound after customer acceptance',
      metadata: { sequence: 3 },
    }),
    buildTestEvent({
      id: 'evt-004',
      type: 'POLICY_ENDORSED',
      timestamp: '2026-01-04T00:00:00.000Z',
      payload: { coverageLimit: 75000, premium: 1500 },
      reasoning: 'Coverage increase endorsed by underwriter',
      metadata: { sequence: 4 },
    }),
  ];
}

